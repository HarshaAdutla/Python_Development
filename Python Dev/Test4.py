
# if else conditions

greeting = "Good Morning"

if greeting == "Good Morning":
    print("If condition satisfied.")
else:
    print("If condition is not satisfied.")

print("If else condition blocks are completed.")    # This line will print after if else conditions bcz it is - starting
# from 0 indent
# Output is :- If condition is not matched.     (Based on condition this will change)
             # If else condition blocks are completed.      (This won't be changed)


# Another Example:-

a = 4

